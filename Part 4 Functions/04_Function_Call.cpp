/*
===========================================
         Function Call in C++
===========================================

Theory:
--------
A function call transfers program control
to the specified function.

Syntax:
function_name();

===========================================
*/

#include <iostream>
using namespace std;

void welcome()
{
    cout << "Welcome to Function Call Example!";
}

int main()
{
    welcome();

    return 0;
}

/*
============ SAMPLE OUTPUT ============

Welcome to Function Call Example!

=======================================
*/